public class shiftright {
    public static void main(String[]args)
    {
        int a=25;        //binary digit for 25-> 1 1 0 0 1
        int b=a>>3;     //left shifting for 2 digits -> 1 1 0 0 1
        System.out.println(b);  //printing the value of b after converting and right shifting
    }
}
