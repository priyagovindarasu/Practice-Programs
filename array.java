public class array {
    public static void main(String args[])
    {
        String[] name=new String[3];    //creating an array using name(array name) and the length is 3

        name[0]="Srini";                // assigning the value for name[0]
        System.out.println(name[0]);    // printing the value of name[0]

        name[1]="Nithya";                //assigning the value for name[1]
        System.out.println(name[1]);

        name[2]="Priya";                //assigning the value for name[2]
        System.out.println(name[2]);

    }
}
